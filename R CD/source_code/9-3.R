# 檢測是否1 == 1
ifelse(1 == 1, "Yes", "No")

# 檢測是否1 == 0
ifelse(1 == 0, "Yes", "No")

# ---------------------------------------------------------- #

toTest <- c(1, 1, 0, 1, 0, 1)
ifelse(toTest == 1, "Yes", "No")

# ---------------------------------------------------------- #

ifelse(toTest == 1, toTest * 3, toTest)

# FALSE引數中的值會在每次檢測結果為FALSE的時候顯示
ifelse(toTest == 1, toTest * 3, "Zero")

# ---------------------------------------------------------- #

toTest[2] <- NA
ifelse(toTest == 1, "Yes", "No")

# ---------------------------------------------------------- #

ifelse(toTest == 1, toTest * 3, toTest)
ifelse(toTest == 1, toTest * 3, "Zero")

